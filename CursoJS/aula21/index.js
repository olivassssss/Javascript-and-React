
/*
Operadores de comparação

> - maior que
>= - maior ou igual a
< - menor que
<= - menor ou igual que
== - igualdade (valor)
=== - igualdade estrita (valor e tipo)
!= - diferente (valor)
!== - diferente estrito (valor e tipo)
*/

console.log(10 > 5)
console.log(10 >= 5)
console.log(10 <5)
console.log(10 <= 5)
console.log(10 == 5)
console.log(10 === 5)
console.log(10 != 5)
